numero = float(input("Ingrese un número: "))

if (numero >= 6) and (numero <= 6.9):
    print("regular") 
elif (numero >= 7) and (numero <= 7.9):
    print("bueno")
elif (numero >= 8) and (numero <= 8.9):
    print("muy bueno") 
elif ( numero >= 9 ) and (numero <= 10): 
    print("exelente") 
else: 
    print("fuera del rango")